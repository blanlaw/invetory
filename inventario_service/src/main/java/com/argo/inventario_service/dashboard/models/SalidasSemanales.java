package com.argo.inventario_service.dashboard.models;

/**
 * The type Salidas semanales.
 */
public class SalidasSemanales {
    private int day;
    private int salidas;

    /**
     * Gets day.
     *
     * @return the day
     */
    public int getDay() {
        return day;
    }

    /**
     * Sets day.
     *
     * @param day the day
     */
    public void setDay(int day) {
        this.day = day;
    }

    /**
     * Gets salidas.
     *
     * @return the salidas
     */
    public int getSalidas() {
        return salidas;
    }

    /**
     * Sets salidas.
     *
     * @param salidas the salidas
     */
    public void setSalidas(int salidas) {
        this.salidas = salidas;
    }
}
